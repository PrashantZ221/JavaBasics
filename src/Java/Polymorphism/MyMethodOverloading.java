package Polymorphism;

public class MyMethodOverloading {

	public void mytest1(int num)
	{
		System.out.println(" myTest1");
	}
	
	public void mytest1(int num, int num2)
	{
		System.out.println("myTest1");
	}
	
	public void mytest1(String i, int num)
	{
		System.out.println("myTest1");
	}
	
	public void mytest1(int num, String j)
	{
		System.out.println("myTest1");
	}

}
